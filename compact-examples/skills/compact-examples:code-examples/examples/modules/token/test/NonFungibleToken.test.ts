// ============================================================================
//  PORT NOTES (what changed from the original ownPublicKey-based test)
// ============================================================================
//  The contract no longer uses `ownPublicKey()` for authorization. Identity is
//  derived inside the proof from a private 32-byte secret. Consequently the test
//  model changed:
//
//   * Identity type: `Either<ZswapCoinPublicKey, ContractAddress>` -> the struct
//     `UserPublicKey { bytes: Uint8Array }`. There is no ContractAddress variant
//     and no zero-address-as-Either; the zero/burn account is the all-zero key.
//     The original "Unsafe Transfer" / contract-recipient cases therefore no
//     longer apply and are dropped.
//   * Caller selection: the original `token.as(CALLER)` switched the prover's
//     coin public key. Here a caller is identified by their SECRET, so we switch
//     the witness private state (`{ userSecretKey }`). `derive(secret)` gives the
//     caller's `UserPublicKey`. A wrong secret can no longer impersonate anyone.
//   * Test driver: the repo ships no OpenZeppelin simulator / `#test-utils`, so
//     this suite drives the compiled mock directly via the Compact JS runtime
//     (the same mechanism the production runtime-verification harness uses).
//
//  PREREQUISITES to run:
//   1. Compile the mock full-ZK:
//        compact compile modules/token/test/mocks/MockNonFungibleToken.compact \
//          modules/token/test/artifacts/MockNonFungibleToken
//   2. Install the EXACT runtime the compiled contract pins (see
//      `checkRuntimeVersion(...)` in the generated index.js; 0.16.0 for
//      compactc 0.31.0 / language 0.23.0):
//        npm i -D @midnight-ntwrk/compact-runtime@<pinned>
//   3. npx vitest run modules/token/test/NonFungibleToken.test.ts
// ============================================================================

